}
-(BOOL)containString:(NSString *)string{
    return [self rangeOfString:string].location != NSNotFound;
}
- (NSDictionary *)parseURLParams{
	NSArray *pairs = [self componentsSeparatedByString:@"&"];
    NSMutableDictionary *muDic = [NSMutableDictionary dictionaryWithCapacity:[pairs count]];
	for(NSString *aPair in pairs){
		NSArray *keyAndValue = [aPair componentsSeparatedByString:@"="];
		if([keyAndValue count] != 2) continue;
        [muDic setObject:[keyAndValue objectAtIndex:1] forKey:[keyAndValue objectAtIndex:0]];
	}
    
	return muDic;
}

//时间戳转时间字符串
- (NSString *)convertToTimeString{
    
    NSDateFormatter *dateFormatter = [[NSDateFormatter alloc] init];
    dateFormatter.timeZone = [NSTimeZone timeZoneWithName:@"Asia/Shanghai"];
    [dateFormatter setDateFormat:@"YYYY-MM-dd"];
    NSDate *theday = [NSDate dateWithTimeIntervalSince1970:[self doubleValue]];
    NSString *timeStr = [NSString stringWithFormat:@"%@",[dateFormatter stringFromDate:theday]];

    return timeStr;
}
//字符串转date
- (NSDate *)convertToDate{
    NSDateFormatter *dateFormatter = [[NSDateFormatter alloc] init];
    [dateFormatter setDateFormat:@"yyyy-MM-dd"];
    NSDate *date = [dateFormatter dateFromString:self];
    return date;
}
-(NSString *)removeChar:(NSString *)string{
    if(![self containString:string]){
        return self;
    }
    
    NSMutableString *mString = [NSMutableString stringWithString:self];
    [mString replaceCharactersInRange:[self rangeOfString:string] withString:@""];
    
    NSString *resultString = [mString removeChar:string];
    
    return resultString;

}

